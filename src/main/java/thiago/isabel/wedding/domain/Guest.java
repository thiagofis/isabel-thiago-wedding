package thiago.isabel.wedding.domain;

public class Guest {

    private String name;
    private int companionsNumber;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCompanionsNumber() {
        return companionsNumber;
    }

    public void setCompanionsNumber(int companionsNumber) {
        this.companionsNumber = companionsNumber;
    }

}
